        possibleWords.append(String(word.dropLast(1)))
    }
    if word.hasSuffix("es") {
        possibleWords.append(String(word.dropLast(2)))
    }
    if word.hasSuffix("ing") {
        possibleWords.append(String(word.dropLast(3)))
    }
    
    
